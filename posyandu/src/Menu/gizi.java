    public void tabel(){
        Object header[]={"NO GIZI","NO KEGIATAN"," NAMA IBU","NO REGIS","NAMA","STATUS"};
        DefaultTableModel data=new DefaultTableModel(null,header);
        tabel.setModel(data);
        setKoneksi();
        String sql="select*from tb_gizi";
        try {
            ResultSet rs=stm.executeQuery(sql);
            while (rs.next())
            {
                String kolom1=rs.getString("kode_gizi");
                String kolom2=rs.getString("kode_kegiatan");
                String kolom3=rs.getString("nama_kegiatan"); 
                String kolom4=rs.getString("no_regis"); 
                String kolom5=rs.getString("nama"); 
                String kolom6=rs.getString("status"); 
                
                String kolom[]={kolom1,kolom2,kolom3,kolom4,kolom5,kolom6};
                data.addRow(kolom);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "data gagal dipanggil"+e);
        }
    }
    
